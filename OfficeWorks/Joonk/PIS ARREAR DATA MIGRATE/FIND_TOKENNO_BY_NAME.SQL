DECLARE
LV_SQL VARCHAR2(1000);
LV_NAME VARCHAR2(100);
BEGIN

FOR C1 IN (
    SELECT NAME FROM  NEW_SAL_ARR_HO
)
LOOP
    LV_NAME := REPLACE(C1.NAME,' ','%');
    LV_NAME := REPLACE(LV_NAME,'.','%');
    
    LV_SQL := NULL;
    LV_SQL := LV_SQL || 'UPDATE NEW_SAL_ARR_HO SET TOKENNO='||CHR(10);
    LV_SQL := LV_SQL || '('||CHR(10);
    LV_SQL := LV_SQL || '    SELECT TOKENNO FROM PISEMPLOYEEMASTER WHERE EMPLOYEENAME LIKE '''||LV_NAME||''''||CHR(10);
    LV_SQL := LV_SQL || ')'||CHR(10);
    LV_SQL := LV_SQL || 'WHERE NAME LIKE '''||LV_NAME||''';'||CHR(10);


--    LV_SQL := 'SELECT * FROM PISEMPLOYEEMASTER WHERE EMPLOYEENAME LIKE '''||LV_NAME||''''||CHR(10)||CHR(10);
    DBMS_OUTPUT.PUT_LINE(LV_SQL);
END LOOP;
END;